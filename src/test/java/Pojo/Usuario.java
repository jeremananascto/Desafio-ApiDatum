package Pojo;

public class Usuario {

    private String name;
    private String job;


    public void setname(String name) {
        this.name = name;
    }

    public String getname() {
        return name;
    }


    public void setJob(String job) {
        this.job = job;
    }

    public String getJob() {
        return job;

    }
}

